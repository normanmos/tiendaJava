    void Mostrar(){
        txtCedula.setText("");
        txtDireccion.setText("");
        txtNombres.setText("");
        txtTelefono.setText("");
        List<Cliente> lista = crud.Mostrar();
        modelo=(DefaultTableModel)tablaClientes.getModel();
        for(int i = 0;i< modelo.getRowCount();i++){
            modelo.removeRow(i);
            i-=1;
        }
        Object[] ob = new Object[6];
        for(int i=0; i<lista.size(); i++){
            ob[0]=lista.get(i).getId();
            ob[1]=lista.get(i).getCedula();
            ob[2]=lista.get(i).getNombre();
            ob[3]=lista.get(i).getDireccion();
            ob[4]=lista.get(i).getTelefono();
            ob[5]=lista.get(i).getEstado();
            
            modelo.addRow(ob);
        }
        tablaClientes.setModel(modelo);
    }
    void Crear(){
        String cedula =txtCedula.getText();
        String nombre = txtNombres.getText();
        String direccion=txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String estado= cmbEstado.getSelectedItem().toString();        
        Object[] ob =new Object[5];
        ob[0]=cedula;
        ob[1]=nombre;
        ob[2]=direccion;
        ob[3]=telefono;
        ob[4]=estado;
        crud.Ingresar(ob);
        Mostrar();
}

    
void Actualizar(){
        int fila=tablaClientes.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debes seleccionar al menos una fila");
        }else{
             String cedula =txtCedula.getText();
            String nombre = txtNombres.getText();
            String direccion=txtDireccion.getText();
            String telefono = txtTelefono.getText();
            String estado= cmbEstado.getSelectedItem().toString();        
            Object[] ob =new Object[6];
            ob[0]=cedula;
            ob[1]=nombre;
            ob[2]=direccion;
            ob[3]=telefono;
            ob[4]=estado;
            ob[5]=cod_cliente;
            crud.Modificar(ob);
            Mostrar();
        }
}

void Eliminar(){
    int fila=tablaClientes.getSelectedRow();
    
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debes seleccionar al menos una fila");
        }else{
            int cod_cliente_=Integer.parseInt(tablaClientes.getValueAt(fila,0).toString());
            crud.Eliminar(cod_cliente_);
            Mostrar();
        }
}
